#Strings 

message = "Hello World"

print(message)

#Advanced Concepts for Strings

message = " Hello! "

print(message[0])
print(message[1])

print(message[-1])

#String Length
print(len(message))

#Removing leading and trailing whitespace
print(message.strip())

#Convert all character to lowercase
print(message.lower())

#Split the string into a list based on the comma
print(message.split(","))

#Operators with Strings

str1 = 'Hello'
str2 = 'World'

print(str1 + " " + str2)
print(str1 * 3)